    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        ArrayList<Torneo> listaTorneos = admin.ListaTorneos();
        int num_torneo = jList2.getSelectedIndex();
        int num_torneofecha = jList1.getSelectedIndex();
        
        if(num_torneo == 0 || num_torneo == 1 || num_torneo == 2)
        {
            IntroducirResultados intRes = new IntroducirResultados(this, usuario, admin, listaTorneos.get(num_torneo));
            intRes.setVisible(true);
            this.dispose();
        }
        else   
        {
            IntroducirResultados intRes = new IntroducirResultados(this, usuario, admin, listaTorneos.get(num_torneofecha));
            intRes.setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed


